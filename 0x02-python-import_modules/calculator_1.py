#!/usr/bin/python3
def add(a, b):
    """My addition function

    Args:
    a: first number
    b: second number

    Returns:
    The return value, a + b
    """
    return(a + b)

def sub(a, b):
    """My substraction function

    Args:
    a: first number
    b: second number

    Returns:
    The return value, a - b
    """
    return(a - b)
def mul(a, b):
    """My multiplication function

    Args:
    a: first number
    b: second number

    Returns:
    The return value. a * b
    """
    return(a * b)
def div(a, b):
    """My division

    Args:
    a: first number
    b: second number

    Return:
     The return value. a / b
    """
    return int(a/b)
